package mltk.util.tuple;

/**
 * Class for <double, double, double> triple.
 * 
 * @author Xiaojie Wang
 * 
 */
public class DoubleTriple {

	public double v1;
	public double v2;
	public double v3;

	/**
	 * Constructor.
	 * 
	 * @param v1 the 1st <code>double</code>.
	 * @param v2 the 2nd <code>double</code>.
	 * @param v3 the 3rd <code>double</code>.
	 */
	public DoubleTriple(double v1, double v2, double v3) {
		this.v1 = v1;
		this.v2 = v2;
		this.v3 = v3;
	}

}